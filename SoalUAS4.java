public class SoalUAS4 {
    public static void main(String[] args) {
        int n = 5;
        int i, j;

        // Lengkapi kode berikut :
        for (i = 0; i < n; i++) {
            for (j = 0; j <= i; j++) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}